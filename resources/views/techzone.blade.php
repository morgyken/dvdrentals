            <h3 class="green">Which Web Server Is Best For You?</h3>
            June 2015
         </div>
         <div class="articleBody">
            <p>When it comes to choosing a web server for hosting a business site 
               the major choice is between the Apache web server and Microsoft’s 
               Internet Information Services web server (Sebesta 2014). Both of these servers data 
               back to the birth of the web in the mid-90s and have for much of that 
               time been the major players in the web server market. In this essay I 
               will first look at the Apache web servers history and capabilities, 
               then the history and capability of Internet Information Services, 
               followed by a look at the possible costs for using both servers, and 
               will conclude with my recommendation on how to choose between the two.</p>
            <p>The Apache web server (hereafter Apache) started in 1995 as a continuation 
               of the NCSA (National Center for Supercomputing Applications) web server 
               (Apache Software Foundation 2014) at the time the most popular web server
               in use (Netcraft 2014a). Apache is free software and distributed under 
               a license which allows not only free use of the software but for modification
               and redistribution of the source code, even for commercial purposes. 
               The Apache project is run by the non-profit Apache Software Foundation. 
               As of November 2014 Apache is still the most popular web server with 
               50.01% of active web sites being hosted on Apache (Netcraft 2014b).</p>
